# While Loop
'''Python has two primitive loop commands:

1- while loops
2- for loops

'''

i = 1
while i<6:
    if i == 4:
        break
    print(i)
    i +=1


j = 1
while j<6:
    if j == 3:
        continue
    print(j)
    j +=1
    
    
'''  
Break : used to terminate the loop when encountered.

Continue : terminates execution in the current iteration & continues execution of the loop
with the next iteration.

'''
